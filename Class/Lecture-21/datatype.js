// Primitive Types: They store a single value.
// Ex- Number, String, Boolean, Undefined, Null, BigInt,

// Non-Primitive Types: They can store collections of data
// Ex- Symbol, Objects.


// 1. Number
let a = 10;
let b = 5;
let c = 1.5;
console.log(a);
console.log("a is : " + typeof(a) + "\n");
console.log(b);
console.log("a is : " + typeof(b) + "\n");
console.log(c);
console.log("c is : " + typeof(c) + "\n");


// 2. String
let name = "Mridul";
console.log(name);
console.log("name is : " + typeof(name) + "\n");

// 3. Boolean
let i = true;
console.log(i);
console.log("i is : " + typeof(i) + "\n");

// 4. Undefined
let x;
console.log("x is : " + typeof(x) + "\n");

// 5. Null
let y = null;
console.log("y is : " + typeof(y) + "\n");
// Type of null is object. It is a bug in JS. By Default, it should be number.

