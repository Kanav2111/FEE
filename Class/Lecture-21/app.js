// Variables in JS

/*
Variables are containers for storing data values. To create a variable in 
JavaScript, you can use the var, let, or const keywords.
*/ 

/*
Defining a variable in JS

Define: To declare a variable in JavaScript, you need to use the var keyword.
Assign: To assign a value to the variable, you need to use the = operator.
Initialize: To initialize a variable, you need to assign a value to it.

1. var : The variable can be re-declared and updated. A global scope
variable.

2. let : The variable cannot be re-declared but can be updated. A block
scope variable.

3. const : The variable cannot be re-declared or updated. A block scope
variable.
*/

// var : re-declaration, initialization, and updating is possible.
// var a = 10;
// var a = 15;
// a = 20;
// console.log(a);

// let : re-declaration is not possible but initialization and updating is possible.
// let a = 10;
// let a = 15;
// a = 20;
// console.log(a);


// const : re-declaration and updating is not possible but initialization is possible.
// const a = 10;
// const a = 15;
// a = 20;
// console.log(a);

/*
Variable Rules
Variable names are case-sensitive; “a” & “A” are different.
Only letters, digits, underscore( _ ) and $ is allowed. White
space are not allowed.
Only a letter, underscore( _ ) or $ should be 1st character.
Reserved words cannot be used as variables names.
*/